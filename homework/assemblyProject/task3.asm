MAIN:  	READ X
	LOAD X
	ADD 1
	BRZERO SUMSTACK
	PUSH
	STACKW COUNT
	BR ICOUNT	

ICOUNT: STORE TEMP 
	LOAD COUNT
	ADD 1
	STORE COUNT
	LOAD TEMP
	WRITE COUNT
	BR MAIN

DCOUNT: STORE TEMP
	LOAD COUNT
	SUB 1
	STORE COUNT
	LOAD TEMP
	BR SUMSTACK

SUMSTACK: LOAD COUNT
	BRZERO EXIT
	STORE TEMP
	STACKR 0
	STORE Y
	WRITE Y
	ADD TEMP
	POP
	BR DCOUNT

EXIT: STORE Y
	WRITE Y 
STOP 
X 0
Y 0
COUNT 0
TEMP 0
